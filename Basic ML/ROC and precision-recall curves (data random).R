# ==============================
# Load libraries
# ==============================
library(dplyr)     # %>% , bind_rows
library(purrr)     # map_df
library(ggplot2)   # qplot, ggplot
library(caret)     # sensitivity, specificity, posPredValue
library(ggrepel)   # geom_text_repel

# ==============================
# Contoh data (ganti dengan data kamu)
# ==============================
set.seed(123)
dataset <- data.frame(
  sex = sample(c("Male", "Female"), 200, replace = TRUE),
  height = rnorm(200, mean = 65, sd = 4)
)

# Jadikan factor dengan level konsisten
dataset$sex <- factor(dataset$sex, levels = c("Female", "Male"))

# Split train-test
set.seed(1)
test_index <- createDataPartition(dataset$sex, times = 1, p = 0.2, list = FALSE)
test_set <- dataset[test_index, ]

# ==============================
# 1. Random guessing example
# ==============================
p <- 0.9
n <- length(test_index)
y_hat <- sample(c("Male", "Female"), n, replace = TRUE, prob=c(p, 1-p)) %>% 
  factor(levels = levels(test_set$sex))
mean(y_hat == test_set$sex)

# ROC curve
probs <- seq(0, 1, length.out = 10)
guessing <- map_df(probs, function(p){
  y_hat <- 
    sample(c("Male", "Female"), n, replace = TRUE, prob=c(p, 1-p)) %>% 
    factor(levels = c("Female", "Male"))
  list(method = "Guessing",
       FPR = 1 - specificity(y_hat, test_set$sex),
       TPR = sensitivity(y_hat, test_set$sex))
})
guessing %>% qplot(FPR, TPR, data =., xlab = "1 - Specificity", ylab = "Sensitivity")

cutoffs <- c(50, seq(60, 75), 80)
height_cutoff <- map_df(cutoffs, function(x){
  y_hat <- ifelse(test_set$height > x, "Male", "Female") %>% 
    factor(levels = c("Female", "Male"))
  list(method = "Height cutoff",
       FPR = 1-specificity(y_hat, test_set$sex),
       TPR = sensitivity(y_hat, test_set$sex))
})

# plot both curves together
bind_rows(guessing, height_cutoff) %>%
  ggplot(aes(FPR, TPR, color = method)) +
  geom_line() +
  geom_point() +
  xlab("1 - Specificity") +
  ylab("Sensitivity")

library(ggrepel)
map_df(cutoffs, function(x){
  y_hat <- ifelse(test_set$height > x, "Male", "Female") %>% 
    factor(levels = c("Female", "Male"))
  list(method = "Height cutoff",
       cutoff = x, 
       FPR = 1-specificity(y_hat, test_set$sex),
       TPR = sensitivity(y_hat, test_set$sex))
}) %>%
  ggplot(aes(FPR, TPR, label = cutoff)) +
  geom_line() +
  geom_point() +
  geom_text_repel(nudge_x = 0.01, nudge_y = -0.01)

